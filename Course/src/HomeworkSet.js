class HomeworkSet{
    constructor(name, status){
        this.name = name; 
        this.status = status;
    }
    get name(){
        return this.name; 
    }
    set name(name){
        return this.name = name;
    }
    get status(){
        return this.status;
    }
    set status(status){
        return this.status = status; 
    }
}   